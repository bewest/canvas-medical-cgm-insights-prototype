DEMO_PHENOTYPES = list(DEMO_SERIES)


def _readings(sgv_values: list) -> list:
    out = []
    for i, sgv in enumerate(sgv_values):
        out.append(
            GlucoseReading(
                epoch_ms=_START_EPOCH_MS + i * _CADENCE_MIN * 60_000,
                sgv=float(sgv),
                direction="Flat",
                device="synthetic://cgm_insights/demo",
            )
        )
    return out


def demo_nightscout(phenotype: str = DEFAULT_PHENOTYPE) -> NightscoutData:
    """Return a NightscoutData bundle for one embedded phenotype series."""
    series = DEMO_SERIES.get(phenotype) or DEMO_SERIES[DEFAULT_PHENOTYPE]
    return NightscoutData(entries=_readings(series))
